CREATE or replace VIEW V_TICKET AS 
	SELECT TICKET_NO,CID,STORE_NO,TICKET_DATE,SUM(FINAL_PRICE) FINAL_PRICE
	FROM TICKET
	GROUP BY TICKET_NO,CID,STORE_NO,TICKET_DATE
	ORDER BY TICKET_DATE;
	
CREATE or replace VIEW V_CUSTOMER AS 
	SELECT CID,ACCOUNT,NAME,SEX,BIRTHDAY,EMAIL,CREATE_TIME,UPDATE_TIME,USER_ROLE
	FROM MEMBER;
	
CREATE or replace VIEW V_LOGINCHECK AS
	SELECT IFNULL(USER_ROLE,'ERROR-0001') USER_ROLE,ACCOUNT,PASSWORD
	FROM MEMBER;